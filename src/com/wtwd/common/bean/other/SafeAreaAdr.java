package com.wtwd.common.bean.other;

public class SafeAreaAdr {
	
	private String longitude;
	private String latitude;
	private String safeRange;
	private String areaName;
	private String areaEffectTime;
	private String safeAddress;
	private String safeId;
	
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getSafeRange() {
		return safeRange;
	}
	public void setSafeRange(String safeRange) {
		this.safeRange = safeRange;
	}
	public String getAreaName() {
		return areaName;
	}
	public void setAreaName(String areaName) {
		this.areaName = areaName;
	}
	public String getAreaEffectTime() {
		return areaEffectTime;
	}
	public void setAreaEffectTime(String areaEffectTime) {
		this.areaEffectTime = areaEffectTime;
	}
	public String getSafeAddress() {
		return safeAddress;
	}
	public void setSafeAddress(String safeAddress) {
		this.safeAddress = safeAddress;
	}
	public String getSafeId() {
		return safeId;
	}
	public void setSafeId(String safeId) {
		this.safeId = safeId;
	}	
}

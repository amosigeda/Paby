package com.wtwd.common.bean.other;

import java.util.List;

public class LocationInfoAdr {

	private String time;
	
	private String upload_time;
	
	private Integer pwr;
	
	private double lon;
	
	private double lat;
	
	private double changeLon;
	
	private double changeLat;
	
	private int battery;
	
	private String locationType;
	
	private double accuracy;
	
	private Integer returnPos;
	
	private LbsInfoAdr lbs;
	
	private List<WifiInfoAdr> wifi;

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}
	
	public String getUpload_time() {
		return upload_time;
	}

	public void setUpload_time(String upload_time) {
		this.upload_time = upload_time;
	}

	public Integer getPwr() {
		return pwr;
	}

	public void setPwr(Integer pwr) {
		this.pwr = pwr;
	}

	public double getLon() {
		return lon;
	}

	public void setLon(double lon) {
		this.lon = lon;
	}

	public double getLat() {
		return lat;
	}

	public void setLat(double lat) {
		this.lat = lat;
	}	

	public double getChangeLon() {
		return changeLon;
	}

	public void setChangeLon(double changeLon) {
		this.changeLon = changeLon;
	}

	public double getChangeLat() {
		return changeLat;
	}

	public void setChangeLat(double changeLat) {
		this.changeLat = changeLat;
	}

	public int getBattery() {
		return battery;
	}

	public void setBattery(int battery) {
		this.battery = battery;
	}
	
	public String getLocationType() {
		return locationType;
	}

	public void setLocationType(String locationType) {
		this.locationType = locationType;
	}

	public double getAccuracy() {
		return accuracy;
	}

	public void setAccuracy(double accuracy) {
		this.accuracy = accuracy;
	}

	public Integer getReturnPos() {
		return returnPos;
	}

	public void setReturnPos(Integer returnPos) {
		this.returnPos = returnPos;
	}

	public LbsInfoAdr getLbs() {
		return lbs;
	}

	public void setLbs(LbsInfoAdr lbs) {
		this.lbs = lbs;
	}

	public List<WifiInfoAdr> getWifi() {
		return wifi;
	}

	public void setWifi(List<WifiInfoAdr> wifi) {
		this.wifi = wifi;
	}

}

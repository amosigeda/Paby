package com.wtwd.sys.innerw.liufeng.domain;

import java.io.Serializable;
import java.util.Date;

import com.godoing.rose.http.PublicVoBean;

public class WSuggestion extends PublicVoBean implements Serializable {

	private static final long serialVersionUID = 3378097133786057002L;
	
	private int id;
	private String user_id;
	private String msg;
	private Date date_time;
	private int belong_project;
	private String photo;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public Date getDate_time() {
		return date_time;
	}
	public void setDate_time(Date date_time) {
		this.date_time = date_time;
	}
	public int getBelong_project() {
		return belong_project;
	}
	public void setBelong_project(int belong_project) {
		this.belong_project = belong_project;
	}
	public String getPhoto() {
		return photo;
	}
	public void setPhoto(String photo) {
		this.photo = photo;
	}
	
}
